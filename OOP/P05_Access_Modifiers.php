<h1>PHP - Access Modifiers</h1>
<p>Properties and methods can have access modifiers which control where they can be accessed.</p>


<p>There are three access modifiers:</p>

<ul>
<ul>public - the property or method can be accessed from everywhere. This is default</ul>
<ul>protected - the property or method can be accessed within the class and by classes derived from that class</ul>
<ul>private - the property or method can ONLY be accessed within the class</ul>
</ul>
